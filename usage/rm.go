package usage

var Rm = `jrnl rm - Remove a page, or post

Usage:

  jrnl rm [id...] [options...]

Options:

  --help  Display this help message

Examples:

  Remove a standalone page:

    $ jrnl rm about

  Remove a standalone post:

    $ jrnl rm introducing-jrnl

  Remove a post in a category:

    $ jrnl rm programming/go-101

  Remove multiple posts, or pages:

    $ jrnl rm contact-me about`
